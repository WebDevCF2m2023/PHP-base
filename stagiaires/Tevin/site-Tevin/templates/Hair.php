    <p class="tee"> Interview by Tevin</p>

    <strong>A propos de l'entreprise</strong>

    <p>Prenez votre mal en patience, ça risque de prende un petit long moment.</p>

    <P>J'ai eu l'occasion d'interviewer Matthias, qui était déjà passé par un autre métier avant de trouver sa voie comme développeur web... de plus qu'il est également alpiniste.</P>
    <!-- Instert something here to click on -->

    C'était marrant et ridicule, aucun de nous ne savait comment enregistrer l'interview, alors j'ai fini par écrire une vingtaine de mots clés.

    <strong>Concernant l'entreprise :</strong>

    <p>Ils sont moins de 100 personnes, ils s'occupent de gérer des applications web utiles à la gestion d'autres appareils.</p>

    <P>Matthias était cordiste (dans son cas, c'était plus précisément pour laver les vitres de bâtiments divers) mais a fini par s'en lasser et s'est reconverti en web dev' après avoir acquis certains bagages.</P>

    <p>Il a étudié à Capsule Académie pour une durée de 10 semaines.</p>

    <!-- Image CA -->

    <p>Ses plus grandes difficultés étant de rester constamment à jour dans le domaine informatique et ce même hors du travail.
        <br>
        Sa façon à lui fut de s'entourer des bonnes personnes, s'abonner à des newsletters et pour finir, les réseaux sociaux.
    </p>

    <p>Les logiciels qui l'aident au quotidien sont Greet, FlexBox et Drible.</p>

    <p>Selon lui (et beaucoup d'autres apparemment), tant qu'il y a la pratique derrière, ne pas avoir beaucoup de théorie ou de diplôme ne risque pas d'être un trop gros problème.</p>

    <p>En effet, tant que la chose est faite, l'employeur sera content, d'après son vécu</p>

    <p>Au final, il ne faut pas avoir peur de montrer ce qu'on sait faire et c'est l'une des seules manières de s'améliorer et en voir les progrès... aussi bien que la vitesse d'adaptation et l'assiduité.</p>





    