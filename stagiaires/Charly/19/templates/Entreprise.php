        <h1> Entreprise</h1><br>
<p>Akaegis est une idée, une réflexion de la façon dont on aborde le travail, les employés arrivent au travail, attendent qu’on leur donne du travail, des directives.</p> 

Mr Lmalak, après des années de travail dans divers domaines, a remarqué que la métrologie dans les divers corps de métier n’a pas changé depuis des années les gens font comme on leur a appris sans le remettre en question.</p><br> 

Akaegis observe le fonctionnement original,qui fait quoi, les différentes relations, le résultat de ces relations, ce qu’on produit dans l’entreprise, le résultat final...</p> 

Akaegis repense toute la méthodologie de manière optimale, on évite les redondances, les pertes de temps, l’attente d’approbation, la validation du supérieur</p><br><br>

 

 

<h1>Akaegis</h1><br>

 
<p>
Akaegis est une société de conception d’idée ou de réorganisation d’un procédé de travail dans le but de mieux faire, d’optimiser le travail, le fait de travailler correctement, on gagne du temps, on fait plus de choses, la productivité est accrue, on fournit plus de services, de produits de qualité.</p><br> 

<p>Un employé arrive au travail, n’attend pas son travail, ses tâches journalières sont sur son ordinateur, quand il a fini ces fameuses tâches, il temps de rentrer à la maison, il aura été efficace, il ne devra pas réfléchir chez lui à ce qu’il doit faire le lendemain.</p><br>

 <p>Beaucoup d’entreprise souffre de cette désorganisation, des gens sont embauché pour faire des choses qu’ils ne sont pas capables de faire ou ont des supérieurs qui n’ont pas fait leur transition digitale, ils sont trop vieux pour réapprendre de nouveau langage, il y a des conflits de générations des nouveaux arrivant ne sachant pas travailler seul de manière autonome, ne sachant pas comment présenter les nouvelles technologies à l’ancienne école sans leur faire comprendre qu’ils sont obsolètes.</p> 
 <br><p>
Le cas de figure suivant est le plus courant : une personne prend sa retraite, on engage une nouvelle personne, qui a étudié dans le même domaine, mais les pratiques ont changé, l’entreprise a gardé le même budget pour la même fonction, on demande à la nouvelle personne de faire comme avant on lui donne les dossiers de son prédécesseur, il doit tout numériser, adapter sa façon de travailler, se faire comprendre de tout le monde, faire des rapports des prédictions pour voir où on va ... 
</p>
