    <strong>Peux-tu me parler de l'organigramme de l'entreprise, où est-ce que tu te situes par rapport aux autres employés ?</strong>

    <p class="themwords">L'organigramme est à la fois horizontal et vertical. L'équivalent du CEO est un groupe de 4 personnes avec chacune une spécialité, finance / industrie / tech / relation client. Dans chaque spécialité, il y a un-e manager principal-e qui gère les managers d'équipes (entre 1 et 10 personnes dans une équipe). Et les managers d'équipe gèrent à leur tour les autres salarié-es. Je suis dans la plus grosse équipe, nous sommes 6 pour le même manager d'équipe. Et c'est le seul manager à gérer d'autres managers d'équipe.</p>

    <strong>Peux-tu me parler d'une activité sur la laquelle tu as dû récemment travailler ?</strong>

    <p class="themwords">Je travaille chez un fournisseur d'énergie et nous avons récemment eu une grosse charge de nouveaux abonnements ; depuis deux semaines, je corrige les bogues signalés par les client-es quand elles ou ils appellent le service Client par téléphone. 
