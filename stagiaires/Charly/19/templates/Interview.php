          <h2>Qu’est-ce qui est le plus dur dans le métier de Web Dev ?</h2>
          <br />

          <p>
            Même si on maitrise tous les langages du monde, on doit toujours
            travailler avec des non-initiés qui ne comprennent aucun de ces
            langages, ils n’ont pas la logique de l’ordinateur, l’ordinateur ne
            fera que ce qu’on lui a donné comme instructions, tous ces lagunages
            ont des limites. Traduire les idées des clients en instructions
            correctes compréhensibles pour l’ordinateur est le plus grand
            challenge.
          </p>
          <br />

          <h2>Comment tu traduis ce que tu fais en langage courant ?</h2>
          <br />

          <p>
            Je prends l’exemple de Ford quand il a créé la base du travail
            industriel. On établit de simples règles à respecter, un résultat
            certain va être obtenu, on part de petites victoires pour construire
            une confiance du processus, on répète la méthode en corrigeant ce
            qui nous induit en erreurs.
          </p>
          <br />

          <h2>Qu’est-ce qui a été dur dans l’apprentissage ?</h2>
          <br />

          <p>
            Discipliner son travail, faire confiance à la méthodologie.
            Vérifier, se baser sur les nouvelles compétences acquises, avancer
            faire des tests avant de produire.
          </p>
          <br />

