              <h2> ATOL FRANCE </h2>
                <h3>"L'innovation et la qualité au service du bien-être"</h3>
                    <p>Atol, association des techniciens en optique et lunetterie, est une chaîne d'opticiens fondée en 1970 qui détient sa propre plateforme logistique et industrielle à Beaune, en France.</p>
                    <p>Avant-guardiste dans le domaine, Atol invente le concept de la seconde paire de lunettes offerte (ATOL BIS) en 1974.</p>
                    <p>Le lancement de leur premier site internet a lieu en 1999.</p>
                    <p>L’année suivante, ils lancent se diversifient dans le marché des appareils auditifs et crééent Atol Audio.</p>
                    <p>En 2005, Atol relocalise une partie de sa production en France en faisant fabriquer les montures de sa première collection privée Ushuaïa dans le Jura.</p>
                    <p>En 2008, Atol devient le premier réseau national d'optique certifié ISO 9001.</p>
                    <p>Le lancement de l'essayage en réalité augmentée sur le site internet et de l'applicationmobile Atol se fait en 2010.</p>
                    <p>La création des lunettes connectées géolocalisables (Téou d'Atol) a lieu en 2015.</p>
                    <p>En 2018, Atol crée la start-up Abeye et lance de nouveaux programmes de recherche. Lepremier programme est Senior Care avec une monture qui détecte les chutes des seniors.</p> 
