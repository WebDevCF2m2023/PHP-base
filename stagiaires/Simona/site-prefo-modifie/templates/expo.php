        <h1>Regarder le passé pour comprendre le présent et anticiper l'avenir</h1>
        <br>
        <div id="expo">
            
       <p>Un regard dans le passé... dans le passé de l'ordinateur, mais aussi la découverte d'une échelle de mesure de l'évolution humaine.</p>
       <p>À ce point, dans toute activité humaine est observée la présence de l'ordinateur. Qu'il s'agisse d'un travail, de loisirs ou simplement d'une recherche d'informations, une chose immatérielle, impliquait tellement de changements matériels.</p>
       <p>Cette petite machine, autrefois de la taille d'une chambre, peut désormais nous transporter à travers le monde, nous mettre en contact avec n'importe qui et répondre à presque toutes les questions.</p>
       <p>En regardant l'évolution de l'ordinateur, les films SF de l'enfance ne semblent plus un chose impossible. Je me souviens à quel point les films SF me semblaient incroyables et intouchables.</p> 
       <p>Maintenant, quand je vois que je peux être servie au restaurant par un robot, je me demande quand viendra la jour où nous irons sur la lune en attendant la fusée comme le bus.</p> 
       <p>À quoi s'attendre ? Nous devrions avoir peur ou participer à cette tempête de changements ? Chaque changement produit des effets, qu'ils soient positifs ou négatifs, il est certain que les choses ne seront plus comme avant.</p>
       <p>Les nouvelles technologies telles que le calcul quantique et l'intelligence artificielle entraîneront le boom de l'industrie informatique.</p>
       <p>Je regarde l'avenir avec peur, mais en même temps avec curiosité.</p> 
       <p>L'évolution ne peut pas être contrôlée, tant que son développement dépend de nombreux facteurs.</p>
       <p>A toi, lecteur, je te propose de faire un petit exercice: fais un pas en arrière et regarde l'évolution comme si tu la découvres pour la première fois. Regarde-la comme un enfant qui voit des flocons de neige pour la première fois et dis-moi ce que tu ressens.</p> 
       <p>J'ai découverte une multitude de sentiments, mais j'ai surtout peur du fait que je n'ai pas trouvé la sécurité. Chose qui me fait penser au fait que l'humanité, dans son évolution technologique et informatique, connaîtra son autodestruction. Le besoin de sécurité est à la base de son évolution et son absence aura une influence négative.</p>
       <p>Si nous voulons profiter des bienfaits de l'évolution, nous devons en supporter les conséquences. Il est impossible de contrôler les effets.</p> 
       <p>Même si nous ne sommes que des observateurs de ce spectacle des changements, nous sommes tous touchés par ses effets.</p> 
       <p>La frontière entre évolution et destruction est mince et tout dépend de la manière dont l'information est utilisée.</p>
       <p>L'information est la chose la plus précieuse, un véritable trésor ou une véritable arme.</p> 
       <p>L'histoire nous montre des nombreux exemples d'évolution et d'autodestruction.</p> 
       <p>Idéalement, nous devrions tirer les leçons des erreurs du passé. Même si la société semble construite selon une matrice bien définie, la réalité est imprévisible.</p> 
       <p>C'est donc à nous de choisir comment nous participerons aux changements de l'humanité.</p>
       <p>Avec la peur dans le cœur, mais aussi avec l'espoir, je regarde vers l'avenir et j'atends de voir l'incroyable spectacle de l'évolution.</p>
       
