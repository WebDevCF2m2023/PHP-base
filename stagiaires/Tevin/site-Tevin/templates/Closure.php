    <h2>Seeking help</h2>

    <p>Cela dépend parfois du pourquoi du comment, mais je déteste demander de l'aide la plupart du temps, surtout si la chose est considérée "simple". 
        <br>
        <br>
        De plus, j'aime pouvoir faire les choses moi-même sans être obligé de dépendre de quelqu'un, voire me considérer comme impuissant sans cette aide. 
    <br>
    <br>
    Mais savoir demander de l'aide est visiblement une chose intégrale dans ce domaine, alors je pense que cette formation sera un bon endroit pour me faire à l'idée de prendre un peu de connaissances chez mes camarades.
    <br>
    <br>
    Il en va de même pour les erreurs, autrement, je me contredis moi-même... 
    <br>
    <br>
    Vouloir s'améliorer est une chose, mais là également, l'environnement jouera un grand rôle. 
    <br>
    <br>
    ... Je pense ne pas avoir à m'en faire pour ça, concernant mon lieu actuel.
    </p>
