    <h2>Un petit mot pour Judith</h2>

    <div id="expoimg2">
        <img src="images/thumb/photo45.jpg"></div>
        <p class="museum2">
        Oublié dans 'Les Femmes Remarquables dans l'Histoire de l'informatique, Judith Love Cohen était une ingénieuse aérospatiale. Ses travaux d'ingénierie comprenaient des travaux sur l'ordinateur de guidage du missile 'Minuteman', the 'Hubble Space Telescope' et sur le système de guidage d'abandon (AGS - de l'Anglais : Abort Guidance System) du module lunaire Apollo.<br> En Avril 1970, L'AGS a joué un rôle important dans le retour en toute sécurité d'Apollo 13 après qu'une explosion du réservoir d'oxygène a paralysé le module de service et forcé les astronautes à utiliser le module lunaire comme 'lifeboat'. 
        Lorsque le désastre a frappé la mission Apollo 13, c'était l'AGS qui a ramené les astronautes chez eux en toute sécurité.</p>
        <p class="museum2">En outre, il manquait toute mention de :-</p>
                <dl id="ladies">
                <ul>
                    <li><dt><a href="#dropFE">Frances Elizabeth Allen</a></dt></li>
                        <dd id="dropFE">Frances Elizabeth Allen était passionnée par l'enseignement et a obtenu son baccalauréat en mathématiques et une mineure en physique à l'Université d'État de New York à Albany. Elle a ensuite obtenu son diplôme de maîtrise à l'Université du Michigan pour être pleinement certifiée en enseignement. Allen a ensuite rejoint IBM pour enseigner le langage de programmation FORTRAN. Par la suite, elle a passé la majeure partie de sa carrière chez IBM à contribuer au développement de techniques de compilation qui ont posé les bases de l'exécution parallèle automatique moderne et des compilateurs d'optimisation modernes.</dd>
                    <li><dt><a href="#dropIR">Ida Rhodes</a></dt></li>
                        <dd id="dropIR">Ida Rhodes, également connue sous le nom de Hadassah Itzkowitz, est une mathématicienne faisant partie d'un groupe influent de femmes dans le développement informatique précoce. Elle a obtenu son baccalauréat et sa maîtrise en          mathématiques à l'Université Cornell et a rejoint le Projet de tables de mathématiques peu de temps après. Rhodes a été l'une des premières développeuses dans l'analyse des systèmes de programmation. Avec son collègue, elle a conçu le langage de programmation C-10. Rhodes a ensuite reçu la médaille d'or pour sa contribution à la conception fonctionnelle pionnière et aux applications de l'équipement informatique numérique électronique.</dd>
                    <li><dt><a href="#dropRP">Radia Joy Perlman</a></dt></li>
                        <dd id="dropRP">Radia Joy Perlman est une programmeuse et ingénieure réseau qui a développé une fascination et un amour pour les mathématiques et les sciences dès son plus jeune âge. Elle a ensuite obtenu son baccalauréat et sa maîtrise du MIT en mathématiques, puis un doctorat en informatique. Perlman est connue pour son invention de l'algorithme de l'arbre couvrant et du protocole de l'arbre couvrant. Ce protocole est devenu essentiel pour aider les ponts réseau à localiser les boucles dans un LAN. Elle a apporté d'importantes contributions à d'autres projets de protocole tels que le CLNP et a reçu de nombreux prix, notamment le prix USENIX pour l'ensemble de ses réalisations.</dd>
                    <li><dt><a href="#dropEF">Elizabeth Feinler</a></dt></li>
                        <dd id="dropEF">Elizabeth Feinler a également étudié un domaine différent avant de se tourner vers le domaine technologique. Elle a travaillé sur son doctorat en biochimie à l'Université Purdue avant de se lancer dans les compilations de données. Elle a rejoint le Centre de recherche sur l'augmentation et a dirigé le Centre d'information sur le réseau pendant deux ans. Feinler et ses collègues ont développé le système d'URL interne que nous utilisons aujourd'hui, qui comprend des textes de domaine tels que .com, .edu, .org, etc. Elle a ensuite travaillé pour la NASA en gérant le NIC de l'Internet scientifique de la NASA. Elle a reçu le prix de service Jonathan B. Postel en 2013 pour sa contribution aux premières étapes du développement d'Internet.</dd>
                    <li><dt><a href="#dropEP">The ENIAC Programmers</a></dt></li>
                        <dd id="dropEP">Les programmeuses de l'ENIAC sont une équipe de six jeunes femmes qui ont travaillé ensemble sur le premier ordinateur programmable entièrement électronique au monde. Kathleen McNulty Mauchly Antonelli, Jean Jennings Bartik, Frances Snyder Holberton, Marlyn Wescoff Meltzer, Frances Bilas Spence et Ruth Lichterman Teitelbaum ont toutes été choisies pour piloter l'ENIAC pendant la Seconde Guerre mondiale. Elles ont programmé à partir de zéro car aucun langage de programmation ou outil n'existait à l'époque. L'équipe utilisait principalement des diagrammes logiques et des calculs de trajectoires balistiques très complexes pour aider l'ENIAC à lancer des trajectoires de missile précises en quelques secondes.</dd>
