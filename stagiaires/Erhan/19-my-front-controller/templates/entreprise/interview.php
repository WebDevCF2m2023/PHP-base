      <details>
        <summary title="">Comment s’organise une journée type ? </summary>
          <p class="interview-content"><br>Je commence en général avant 9:00, je revois ce que j'ai fait la veille avant le daily stand up qui est une réunion qui a lieu tous les matins sur Slack afin de faire le point avec toute l'équipe.
            On travaille en mode scrum, donc on organise les tâches à faire sur deux semaines, le matin si je ne suis pas encore en train de travailler sur une story, j'en prends une nouvelle... 
            En dehors de ça on est assez libre de s'organiser comme on veut... 
 
            <br>
            Les différentes réunions: 
            <br>
            - Backlog refinment : proxy (personne qui fait la liaison entre les dev et le business (client)) explique les différentes tâches qui seront exécutées lors du prochain sprint.
            Suite à cette réunion on se répartit les différentes tâches à analyser (on fait du pair model).
            <br>
            - Backlog estimation : on se réunit pour expliquer l'analyse et on estime un certain nombre de point pour la story.
            <br>
            - A la fin du sprint (deux semaines) on fait la réunion rétrospective afin de discuter de ce qui s'est bien passer ou mal durant le sprint, de là on en tire des actions afin de régler les éventuels problèmes.
            <br>
            - Juste après la rétrospective on fait la réunion Sprint Kickoff, là on définit les ressources disponibles à partir de là, on a une idée du nombre de story que l'on pourra réaliser durant le prochain sprint.
            <br>
            - Le mercredi matin on a une réunion entre développeurs qui s’appelle code review, là on parle des nouveaux développements, problèmes techniques rencontrés ou de gros refactoring nécessaire...  </p>
      </details>
