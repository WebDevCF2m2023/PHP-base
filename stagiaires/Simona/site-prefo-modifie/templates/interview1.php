                <div id="interview">

                
                     <div class="a"><h2>Je veux commencer par vous remercier pour votre temps et votre gentillesse. Quel est votre nom ?</h2><br>
                     Je m'appelle Mitrache Valentin et je travaille dans le domaine du web depuis environ trois ans.</div><br>
                     <div class="b"><h2>Es-tu indépendant ?</h2><br>
                     Oui, indépendant.</div><br>
                     <div class="c"><h2>Comment se déroule une journée type, quelles sont vos tâches, que faites-vous habituellement ?</h2><br>
                     Je dirais que cela diffère d'un jour à l'autre car je suis du côté indépendant.
                     Parfois, il m'arrive de me réveiller et de commencer immédiatement à coder parce que j'ai vraiment envie de terminer quelque chose ou d'autres fois, je reste juste un peu libre et je me détends de temps en temps.</div><br>
                     <div class="d"><h2>Quel est ton parcours dans le domaine du web ?</h2><br>
                     J'ai commencé en 2020, au départ je n'étais pas sur le web, j'étais avec un langage de programmation python, mais au bout de quelques mois depuis que je combattais j'ai découvert le web et depuis j'ai commencé avec les bases du web HTML, CSS , JavaScript et j'ai continué ainsi jusqu'à ce que j'arrive là où je suis maintenant.</div><br>
                     <div class="e"><h2>Quelles sont les principales activités ? Maintenance, front-end ou back-end ?</h2><br>
                     Parce que je suis indépendant, je fais encore parfois face aux deux, mais d'habitude front-end, je dirais.</div><br>
                     <div class="f"><h2>Qu'est-ce qui vous a attiré dans ce métier, qu'est-ce qui vous plaît le plus ?</h2><br>
                     Je dirais que je peux travailler assez librement où que je sois et c'est un domaine qui me fascine vraiment étant donné que j'aime beaucoup la technologie.</div><br>
                     <div class="g"><h2>Quels conseils donneriez-vous à un développeur en devenant?</h2><br>
                     N'arrêtez pas d'apprendre, apprenez le plus possible, cherchez une équipe et réalisez des projets ensemble car c'est un avantage de savoir travailler en équipe pour postuler à un emploi.</div><br>
                     <div class="h"><h2>A quel point a-t-il été difficile pour vous de trouver des projets ?</h2><br>
                     Comme je suis sur des plateformes de freelance, des clients me contactent, mais c'est assez difficile à trouver, surtout si l'on débute et que l'on n'a pas de portfolio.</div><br>
                     <div class="i"><h2>Etes-vous satisfait de vos revenus et de vos avantages sociaux ?</h2><br>
                     Je dirais oui, car par rapport à ce que je fais, c'est plutôt correct.</div><br>
                     <div class="j"><h2>Comment un développeur peut-il développer? Comment peut-il évoluer ?</h2><br>
                     Travail d'équipe, apprentissage. À un moment donné, j'ai également fait équipe avec des amis et j'ai eu une sorte de brainstorming. Parfois nous étions trois, quatre et nous parlions de comment évoluer et faire des projets en équipe. C'est donc assez important aussi.</div><br>
                     <div class="k"><h2>À quoi un développeur doit-il prêter attention, sur quoi il doit se concentrer ?</h2><br>
                     Je dirais que quoi qu'il fasse, il doit se concentrer, être prudent car au début il peut y avoir pas mal de frustrations car la précipitation gâche le travail.</div><br>
                     <div class="l"><h2>À quel point ce travail est-il stressant ?</h2><br>
                     Cela peut devenir assez stressant si tu ne te prépares pas suffisamment pour un projet, si tu n'étudies pas correctement, mais il y a aussi des moments où tu fais certaines choses pour le plaisir, donc ce n'est pas toujours juste du stress.</div><br>
                     <div class="m"><h2>Qu'est-ce qui rend un développeur meilleur qu'un autre ?</h2><br>
                     Travail en équipe. C'est l'élément le plus important, je dirais, savoir travailler en équipe.</div><br>
                     <div class="n"><h2> Comment voyez-vous l'avenir de ce métier ?</h2><br>
                     Je dirais que ça va beaucoup évoluer, il y a une demande sur le marché, on recherche des développeurs, ça ne va pas diminuer du tout.</div><br>
                     <div class="o"><h2>Avec quel langage de programmation travaillez-vous ?</h2><br>
                     Être dans le web, JavaScript, mais ça ne s'arrête pas là car on peut développer des applications web aussi bien avec Python qu'avec Java.</div><br>
                     <div class="p"><h2>Quelle erreur de programmation est impardonnable pour vous ?</h2><br>
                     Il n'y a rien d'impardonnable. Des erreurs peuvent être commises, mais elles peuvent être corrigées.</div><br>
                     <div class="q"><h2>Quels changements apporteriez-vous à votre travail ?</h2><br>
                     Aucun, c'est parfait comme ça.</div><br>
                     <div class="r"><h2>Dans cinq ans, vous voyez-vous toujours travailler dans ce domaine?</h2><br>
                     Oui, bien sûr.</div><br>
                     <div class="s"><h2>Merci pour votre temps. Je vous souhaite une bonne continuation.</h2><br>
