        Et pour finir... selon toi, quelles sont les avantages et désavantages/contraintes à devenir web développeur ? 
        <br>
        <br>
        Et aussi, quelle serait les connaissances requises pour vouloir commencer à postuler ? 
    </strong>

    <p class="themwords">La contrainte est qu'il faut accepter de travailler avec d'autres et donc, savoir gérer les conflits, savoir communiquer correctement. 
        <br>
        Le gros avantage est que tu es en contrôle total de ce que tu codes ; tu peux faire ce que tu veux, le code va toujours obéir. 
        <br>
        Le désavantage est la responsabilité qui va avec. Tu dois avoir conscience que le code est puissant et que tu peux tout casser. 
        <br>
        <br>
        Pour commencer à postuler, les notes de l'école et ta motivation devraient suffire. 
        <br>
        Rapproche-toi d'associations ou de structures de ce genre pour demander un coup de main sur la gestion du site. 
        <br>
        Commence à créer ton site ou n'importe.  
        <br>
        L'essentiel c'est que tu montres ce que tu sais faire.
